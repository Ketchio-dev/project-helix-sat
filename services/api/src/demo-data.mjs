function createChoices(choices) {
  return choices.map(({ label, text }) => ({ key: label, label, text }));
}

function createItem({
  itemId,
  section,
  domain,
  skill,
  difficulty_band,
  item_format,
  stem,
  passage = '',
  choices,
  answerKey,
  status = 'production',
  tags = [],
  estimatedTimeSec = 75,
}) {
  return {
    itemId,
    section,
    domain,
    skill,
    difficulty_band,
    item_format,
    stem,
    prompt: stem,
    passage,
    choices: createChoices(choices),
    answerKey,
    status,
    tags,
    estimatedTimeSec,
  };
}

function createRationale({
  item_id,
  explanation,
  wrongRationales,
  misconceptionByChoice,
  hint_ladder,
  misconception_tags,
}) {
  return {
    item_id,
    explanation,
    canonical_correct_rationale: explanation,
    canonical_wrong_rationales: wrongRationales,
    misconceptionByChoice,
    hint_ladder,
    hint_ladder_json: hint_ladder,
    misconception_tags,
  };
}

